public class Syntax {
    public static void main(String[] args) {
        int a=5,b=6;

        //Bitwise And (&)
        System.out.println(a&b);

        //Bitwise OR (|)
        System.out.println(a|b);


        //Bitwise XOR(^)
        System.out.println(a^b);


        //Binary One's Compliment
        System.out.println(~a);

        //Binary left shift (<<)
        System.out.println(5<<2);

        //binary Right shift(>>)
        System.out.println(6>>1);
    }
}